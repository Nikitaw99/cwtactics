package org.wolftec.cwt.model.gameround;

public class Position {

  private int x;
  private int y;

  public int getPositionX() {
    return x;
  }

  public int getPositionY() {
    return y;
  }

  public void setPositionX(int newX) {
    x = newX;
  }

  public void setPositionY(int newY) {
    y = newY;
  }
}
