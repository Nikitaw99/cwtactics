package net.temp.wolfTecEngine.renderer;

/**
 * A direction between two tiles on the map.
 */
public enum Direction {

  /**
   * <strong>result:</strong> y-1
   */
  UP,

  /**
   * <strong>result:</strong> y+1
   */
  DOWN,

  /**
   * <strong>result:</strong> x-1
   */
  LEFT,

  /**
   * <strong>result:</strong> x+1
   */
  RIGHT;

}
